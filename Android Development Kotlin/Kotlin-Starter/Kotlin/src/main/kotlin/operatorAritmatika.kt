fun main(){
    val numberOne = 10
    val numberTwo = 2

    println("==================================")
    println("==== Penjumlahan =====")
    println("==================================")
    println(numberOne + numberTwo)
    println()

    println("==================================")
    println("==== Pengurangan =====")
    println("==================================")
    println(numberOne - numberTwo)
    println()

    println("==================================")
    println("==== Perkalian =====")
    println("==================================")
    println(numberOne * numberTwo)
    println()

    println("==================================")
    println("==== pembagian =====")
    println("==================================")
    println(numberOne / numberTwo)
    println()

    println("==================================")
    println("=  Contoh perhitungan matematika =")
    println("==================================")
    println(5 + 4 * 4)
    println()

    println("=======================================================================")
    println("ika ingin operasi 5 + 4 dilakukan terlebih dahulu, gunakan tanda kurung")
    println("=======================================================================")
    println((5 + 4) * 4)
    println()
}